#5)შექმენი რაიმე სია და მომხმარებელს შემოატანინე n რიცხვი 1-5 დიაპაზონში,
#შემდეგ კი გამოიტანე ამ სიაში ყველა n რიგის რიცხვი (მაგალითად, სიაში [1,2,3,4,5,6] თუ მომხმარებელმა შემოიტანა 2, შენ გამოიტან [2,4,6])

n =(1,2,3,4,5)             

num=int(input("enter number"))
for i in n:
    if i % num==0:
        print(i+2)


